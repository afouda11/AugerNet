# ═══════════════════════════════════════════════════════════════════════════════
#  Parse a predict-mode _labels.txt file
# ═══════════════════════════════════════════════════════════════════════════════

def parse_labels_file(path):
    """Parse a predict-mode ``_labels.txt`` and return mol_name → mean carbon CEBE.

    Returns
    -------
    dict : {str: float}
        Mapping of molecule name (e.g. ``'methane_00'``) to the mean
        predicted CEBE across all carbon atoms in that structure.
    """
    results = {}
    current_mol = None
    carbon_preds = []

    with open(path) as f:
        for line in f:
            line = line.rstrip('\n')

            # Molecule header: # --- methane_00 ---
            m = re.match(r'^# --- (.+?) ---', line)
            if m:
                # Save previous molecule
                if current_mol is not None:
                    results[current_mol] = (
                        np.mean(carbon_preds) if carbon_preds else np.nan
                    )
                current_mol = m.group(1)
                carbon_preds = []
                continue

            # Skip other comment / blank lines
            if line.startswith('#') or line.strip() == '':
                continue

            # Data line:  "  C      290.3692" or "  H*      -3.4282"
            parts = line.split()
            if len(parts) >= 2:
                sym = parts[0]
                val = float(parts[1])
                # Carbon atom (no * marker)
                if sym == 'C':
                    carbon_preds.append(val)

    # Save last molecule
    if current_mol is not None:
        results[current_mol] = (
            np.mean(carbon_preds) if carbon_preds else np.nan
        )

    return results


# ═══════════════════════════════════════════════════════════════════════════════
#  Load predictions (shared helper)
# ═══════════════════════════════════════════════════════════════════════════════

def _load_model_predictions():
    """Return dict  model_label → {mol_name: mean_carbon_cebe}."""
    model_preds = {}
    for mcfg in MODEL_CONFIGS:
        path = os.path.join(PREDICT_DIR, mcfg['labels_file'])
        if not os.path.exists(path):
            print(f"    Labels file not found: {path}")
            print(f"    Run:  python -m augernet --config configs/predict_pes_EQ.yml")
            return None
        preds = parse_labels_file(path)
        model_preds[mcfg['label']] = preds
        print(f"  Loaded {len(preds)} predictions from {mcfg['labels_file']}")
    return model_preds


# ═══════════════════════════════════════════════════════════════════════════════
#  Plot a single PES panel
# ═══════════════════════════════════════════════════════════════════════════════

def _plot_pes_panel(ax, mol_name, mol_info, model_preds, *,
                    panel_label=None, show_ylabel=True, show_legend=True):
    """Draw one PES comparison panel on *ax*.

    Parameters
    ----------
    ax : matplotlib Axes
    mol_name : str  – key into the PES reference files (e.g. 'methane')
    mol_info : dict – metadata (coord_label, eq_bond, multiref, …)
    model_preds : dict – model_label → {mol_name_XX: cebe}
    panel_label : str or None – e.g. '(a)' drawn in the top-left corner
    show_ylabel : bool
    show_legend : bool
    """
    coord_label = mol_info['coord_label']

    # Load DFT reference PES (coordinate + single-ref CEBE)
    pes_file = os.path.join(PES_REF_DIR, f'{mol_name}_pes.txt')
    if not os.path.exists(pes_file):
        print(f'  ⚠  PES file not found: {pes_file}')
        return
    pes_arr   = np.loadtxt(pes_file)
    coords    = pes_arr[:, 0]
    calc_cebe = pes_arr[:, 1]
    n_struct  = len(coords)

    # Plot DFT single-reference
    ax.plot(coords, calc_cebe, 'o-', color='tab:blue', linewidth=2,
            markersize=5, label='MC-PDFT', zorder=10)

    # Plot multi-reference if available
#     multiref_path = mol_info.get('multiref')
#     if multiref_path and os.path.exists(multiref_path):
#         mr = np.loadtxt(multiref_path)
#         ax.plot(mr[:, 0], mr[:, 1], '^-', color='tab:purple',
#                 linewidth=2, markersize=5, label='Multi-ref.', zorder=9)

    # Plot each model's GNN predictions & write CSVs
    for m_idx, mcfg in enumerate(MODEL_CONFIGS):
        label = mcfg['label']
        preds = model_preds[label]

        gnn_cebe = np.array([
            preds.get(f'{mol_name}_{i:02d}', np.nan)
            for i in range(n_struct)
        ])

        color  = MODEL_COLORS[m_idx % len(MODEL_COLORS)]
        marker = MODEL_MARKERS[m_idx % len(MODEL_MARKERS)]
        ax.plot(coords, gnn_cebe, f'{marker}--', color=color,
                linewidth=2, markersize=5, label=label)

        # Stats
        valid = ~np.isnan(gnn_cebe)
        if valid.sum() > 0:
            mae = np.mean(np.abs(gnn_cebe[valid] - calc_cebe[valid]))
            print(f'  {mol_name:<18s}  {label:<10s}  '
                  f'MAE = {mae:.4f} eV  ({valid.sum()} pts)')

        # Write CSV
        csv_stem = mcfg['labels_file'].replace('_labels.txt', '')
        csv_path = os.path.join(
            PES_DIR, f'pes_{mol_name}_{csv_stem}.csv',
        )
        np.savetxt(
            csv_path,
            np.column_stack([coords, calc_cebe, gnn_cebe]),
            delimiter=',',
            header='coordinate,calc_cebe,gnn_cebe',
            comments='',
            fmt='%.10f',
        )

    # Equilibrium reference line
    eq_bond = mol_info.get('eq_bond')
    if eq_bond is not None:
        ax.axvline(eq_bond, color='black', ls=':', lw=1.5, alpha=0.7)

    ax.set_xlabel(coord_label, fontsize=14, fontweight='bold')
    if show_ylabel:
        ax.set_ylabel('CEBE (eV)', fontsize=14, fontweight='bold')
    title = mol_info.get('title', mol_name.capitalize())
    #ax.set_title(title, fontsize=16, fontweight='bold')
    if show_legend:
        ax.legend(fontsize=11, framealpha=0.85, fancybox='round')
    ax.grid(True, alpha=0.2)
    ax.tick_params(axis='both', labelsize=12)

    xlim = mol_info.get('xlim')
    if xlim is not None:
        ax.set_xlim(xlim)
    ylim = mol_info.get('ylim')
    if ylim is not None:
        ax.set_ylim(ylim)

    if panel_label is not None:
        ax.text(0.03, 0.95, panel_label, transform=ax.transAxes,
                fontsize=16, fontweight='bold', va='top')


