# ═══════════════════════════════════════════════════════════════════════════════

def main():
    print('=' * 72)
    print('  PES Predict-Mode Comparison')
    print('=' * 72)

    # ── Load predictions from each model's labels file ────────────────────
    model_preds = _load_model_predictions()
    if model_preds is None:
        return

    # ── Plot ──────────────────────────────────────────────────────────────
    sns.set_style('whitegrid')
    n_mols = len(MOLECULES)
    fig, axes = plt.subplots(1, n_mols, figsize=(6 * n_mols, 5))
    if n_mols == 1:
        axes = [axes]

    for ax_idx, (ax, (mol_name, mol_info)) in enumerate(
        zip(axes, MOLECULES.items())
    ):
        _plot_pes_panel(
            ax, mol_name, mol_info, model_preds,
            show_ylabel=(ax_idx == 0),
            show_legend=(ax_idx == 0),
        )

    plt.tight_layout()
    png_path = os.path.join(PNG_DIR, 'pes_predict_comparison.png')
    pdf_path = os.path.join(PNG_DIR, 'pes_predict_comparison.pdf')
    fig.savefig(png_path, dpi=300, bbox_inches='tight')
    fig.savefig(pdf_path, bbox_inches='tight')
    print(f'\n  ✓ PES 2-panel → {png_path}')
    print(f'  ✓ PES 2-panel → {pdf_path}')
    plt.close(fig)

    print('\n  ✅  Done (2-panel).')


